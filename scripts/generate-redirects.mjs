/**
 * Generate Cloudflare Pages `_redirects` file from SLUG_MAP.
 *
 * Emits 301 redirects for indicator URLs that changed from internal `ind_*`
 * IDs to SEO-friendly slugs. Runs in `prebuild` so the redirects file is
 * always in sync with the slug map at build time.
 *
 * Output: public/_redirects (overwritten on each run)
 *
 * Format per Cloudflare Pages docs:
 *   <source-path>    <destination-path>    <status>
 *
 * 50 redirects total (25 indicators × 2 locales).
 */
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const OUT = resolve(ROOT, 'public', '_redirects');

// Import SLUG_MAP from the TS source via a runtime-friendly approach.
// We avoid bundlers here — read and parse the lookups.ts to extract SLUG_MAP.
import { readFileSync } from 'node:fs';
const lookupsSource = readFileSync(resolve(ROOT, 'src/data/lookups.ts'), 'utf8');

// Extract the SLUG_MAP literal. Pattern: `export const SLUG_MAP: Record<string, string> = { ... };`
const slugMapMatch = lookupsSource.match(
  /export const SLUG_MAP: Record<string, string> = \{([\s\S]*?)\};/,
);
if (!slugMapMatch) {
  console.error('generate-redirects: could not locate SLUG_MAP in src/data/lookups.ts');
  process.exit(1);
}

const slugMapBody = slugMapMatch[1];
// Parse `key: 'value',` pairs.
const entries = [];
const pairRe = /(\w+)\s*:\s*'([^']+)'/g;
let m;
while ((m = pairRe.exec(slugMapBody)) !== null) {
  entries.push([m[1], m[2]]);
}

if (entries.length === 0) {
  console.error('generate-redirects: SLUG_MAP appears empty');
  process.exit(1);
}

// Build the _redirects file.
const lines = [
  '# Cloudflare Pages redirects — auto-generated by scripts/generate-redirects.mjs',
  '# Source-of-truth: src/data/lookups.ts SLUG_MAP',
  '# Do not edit by hand. Re-run via `npm run build` (or directly: `node scripts/generate-redirects.mjs`).',
  `# Generated: ${new Date().toISOString()}`,
  '',
  '# Indicator slug rewrites — old ind_* paths 301-redirect to SEO slugs.',
];
for (const [oldId, newSlug] of entries) {
  lines.push(`/en/indicators/${oldId}/    /en/indicators/${newSlug}/    301`);
  lines.push(`/th/indicators/${oldId}/    /th/indicators/${newSlug}/    301`);
}
lines.push('');

// Ensure public/ exists.
mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, lines.join('\n'), 'utf8');

console.log(
  `generate-redirects: wrote ${entries.length * 2} redirect rules (${entries.length} indicators × 2 locales) to ${OUT}`,
);
